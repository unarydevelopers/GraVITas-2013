package com.gravitas2013.app;

public class FeedJson {
	int id;
	String data;
	String title;
	String time;
	String category;
	String color;
	String thumb;
	public FeedJson(int id, String data, String title, String time,
			String category, String color, String thumb) {
		super();
		this.id = id;
		this.data = data;
		this.title = title;
		this.time = time;
		this.category = category;
		this.thumb = thumb;
		this.color = color;
	}
	
	

}
